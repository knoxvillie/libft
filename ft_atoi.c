int	ft_atoi(const char *ptr)
{
	int	sign;
	int	res;

	sign = 1;
	res = 0;
	while ((*ptr == '\f') || (*ptr == '\n') || (*ptr == '\r')
		|| (*ptr == '\t') || (*ptr == '\v') || (*ptr == ' '))
		ptr++;
	if ((*ptr == '+') || (*ptr == '-'))
	{
		if (*ptr == '-')
			sign *= -1;
		ptr++;
	}
	while ((*ptr >= '0') && (*ptr <= '9'))
	{
		res *= 10;
		res += (*ptr - 48);
		ptr++;
	}
	return (res * sign);
}
